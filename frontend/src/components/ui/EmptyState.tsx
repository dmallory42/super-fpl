import { ReactNode } from 'react'

interface EmptyStateProps {
  icon?: ReactNode
  title: string
  description?: string
  action?: {
    label: string
    onClick: () => void
  }
  className?: string
}

export function EmptyState({
  icon,
  title,
  description,
  action,
  className = '',
}: EmptyStateProps) {
  return (
    <div
      className={`
        flex flex-col items-center justify-center py-12 px-4 text-center
        animate-fade-in-up
        ${className}
      `}
    >
      {icon && (
        <div className="mb-4 text-foreground-dim opacity-50">{icon}</div>
      )}
      <h3 className="font-display text-lg uppercase tracking-wider text-foreground mb-2">
        {title}
      </h3>
      {description && (
        <p className="text-foreground-muted text-sm max-w-md">{description}</p>
      )}
      {action && (
        <button onClick={action.onClick} className="btn-primary mt-4">
          {action.label}
        </button>
      )}
    </div>
  )
}

